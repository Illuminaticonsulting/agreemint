/**
 * Get a translation function for a given language.
 */
function getTranslation(lang) {
  const bundle = TRANSLATIONS[lang] || TRANSLATIONS.en;
  const fallback = TRANSLATIONS.en;
  return function t(key) {
    return bundle[key] || fallback[key] || key;
  };
}

/**
 * Get the full translation bundle for a language (for frontend delivery).
 */
function getLanguageBundle(lang) {
  const code = lang || 'en';
  const meta = SUPPORTED_LANGUAGES[code] || SUPPORTED_LANGUAGES.en;
  return {
    lang: code,
    ...meta,
    translations: TRANSLATIONS[code] || TRANSLATIONS.en,
    fallback: code !== 'en' ? TRANSLATIONS.en : undefined
  };
}

/**
 * Detect preferred language from Accept-Language header.
 */
function detectLanguage(acceptHeader) {
  if (!acceptHeader) return 'en';
  const supported = Object.keys(SUPPORTED_LANGUAGES);
  const preferred = acceptHeader.split(',').map(l => {
    const [lang, q] = l.trim().split(';q=');
    return { lang: lang.split('-')[0].toLowerCase(), q: parseFloat(q) || 1 };
  }).sort((a, b) => b.q - a.q);

  for (const p of preferred) {
    if (supported.includes(p.lang)) return p.lang;
  }
  return 'en';
}

